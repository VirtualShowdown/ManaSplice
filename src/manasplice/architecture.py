def _event_bus_text() -> str:
    return f'''{ARCHITECTURE_MARKER}
from __future__ import annotations

from collections import defaultdict
from collections.abc import Callable
from typing import Generic, TypeVar

EventT = TypeVar("EventT")


class EventBus(Generic[EventT]):
    def __init__(self) -> None:
        self._handlers: dict[str, list[Callable[[EventT], None]]] = defaultdict(list)

    def subscribe(self, event_name: str, handler: Callable[[EventT], None]) -> None:
        self._handlers[event_name].append(handler)

    def publish(self, event: EventT) -> None:
        event_name = getattr(event, "name", event.__class__.__name__)
        for handler in tuple(self._handlers[event_name]):
            handler(event)
'''


def _pipeline_text() -> str:
    return f'''{ARCHITECTURE_MARKER}
from __future__ import annotations

from collections.abc import Callable
from dataclasses import dataclass
from typing import Generic, TypeVar

InT = TypeVar("InT")
OutT = TypeVar("OutT")


@dataclass(frozen=True, slots=True)
class Pipeline(Generic[InT, OutT]):
    steps: tuple[Callable[..., object], ...]

    def then(self, step: Callable[..., object]) -> "Pipeline[InT, OutT]":
        return Pipeline((*self.steps, step))

    def __call__(self, value: InT) -> OutT:
        result: object = value
        for step in self.steps:
            result = step(result)
        return result  # type: ignore[return-value]


def pipe(value: InT, *steps: Callable[..., object]) -> OutT:
    return Pipeline[InT, OutT](steps)(value)
'''


def _lint_layer_dependencies(project_root: Path) -> dict[str, str]:
    contexts_root = project_root / "contexts"
    if not contexts_root.exists():
        return {}
    violations: dict[str, str] = {}
    for file_path in contexts_root.rglob("*.py"):
        if _is_ignored(file_path, project_root):
            continue
        location = _layer_location(file_path, project_root)
        if location is None:
            continue
        context_name, layer_name = location
        for imported_module, line_number in _iter_imported_modules(file_path, project_root):
            violation = _layer_violation(context_name, layer_name, imported_module)
            if violation:
                key = f"{file_path.relative_to(project_root)}:{line_number}"
                violations[key] = violation

    for file_path in (project_root / "shared").rglob("*.py") if (project_root / "shared").exists() else []:
        if _is_ignored(file_path, project_root):
            continue
        for imported_module, line_number in _iter_imported_modules(file_path, project_root):
            if imported_module.startswith("contexts."):
                key = f"{file_path.relative_to(project_root)}:{line_number}"
                violations[key] = "shared code must not import bounded contexts"
    return violations


def _layer_location(file_path: Path, project_root: Path) -> tuple[str, str] | None:
    try:
        parts = file_path.relative_to(project_root).parts
    except ValueError:
        return None
    if len(parts) < 4 or parts[0] != "contexts":
        return None
    context_name = parts[1]
    layer_name = parts[2]
    if layer_name not in {"domain", "application", "infrastructure", "presentation"}:
        return None
    return context_name, layer_name


def _iter_imported_modules(file_path: Path, project_root: Path) -> list[tuple[str, int]]:
    try:
        tree = ast.parse(file_path.read_text(encoding="utf-8"))
    except (OSError, SyntaxError):
        return []
    imports: list[tuple[str, int]] = []
    current_module = _module_name(file_path, project_root)
    for node in ast.walk(tree):
        if isinstance(node, ast.Import):
            imports.extend((alias.name, node.lineno) for alias in node.names)
        elif isinstance(node, ast.ImportFrom):
            imports.append((_resolve_import_from(current_module, node), node.lineno))
    return imports


def _module_name(file_path: Path, project_root: Path) -> str:
    relative = file_path.relative_to(project_root).with_suffix("")
    return ".".join(part for part in relative.parts if part != "__init__")


def _resolve_import_from(current_module: str, node: ast.ImportFrom) -> str:
    if node.level == 0:
        return node.module or ""
    package_parts = current_module.split(".")[:-1]
    if node.level > 1:
        package_parts = package_parts[: -(node.level - 1)]
    if node.module:
        package_parts.extend(part for part in node.module.split(".") if part)
    return ".".join(package_parts)


def _layer_violation(context_name: str, layer_name: str, imported_module: str) -> str:
    imported_parts = imported_module.split(".")
    if len(imported_parts) < 3 or imported_parts[0] != "contexts":
        return ""
    imported_context = imported_parts[1]
    imported_layer = imported_parts[2]
    if imported_context != context_name:
        return f"bounded context '{context_name}' must not import context '{imported_context}'"
    forbidden_layers = {
        "domain": {"application", "infrastructure", "presentation"},
        "application": {"infrastructure", "presentation"},
        "infrastructure": {"presentation"},
        "presentation": {"infrastructure"},
    }
    if imported_layer in forbidden_layers.get(layer_name, set()):
        return f"{layer_name} layer must not import {imported_layer} layer"
    return ""


def _is_ignored(file_path: Path, project_root: Path) -> bool:
    current = file_path.parent.resolve()
    project_root = project_root.resolve()
    while True:
        if (current / ".msignore").exists():
            return True
        if current == project_root or current.parent == current:
            return False
        current = current.parent


def _build_preview_diffs(file_changes: list[FileChange]) -> list[str]:
    diffs: list[str] = []
    for change in file_changes:
        diff_lines = list(
            difflib.unified_diff(
                change.before_text.splitlines(),
                change.after_text.splitlines(),
                fromfile=str(change.path),
                tofile=str(change.path),
                lineterm="",
            )
        )
        if diff_lines:
            diffs.append("\n".join(diff_lines))
    return diffs

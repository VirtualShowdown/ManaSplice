from .utils import write_text_preserving_newlines

ARCHITECTURE_MARKER = "# Auto-generated by ManaSplice: layered architecture"


@dataclass(slots=True)
class ArchitectureOptions:
    preview: bool = False
    validate: bool = False
    contexts: list[str] | None = None


def transform_project_to_layered_architecture(
    project_root: Path,
    *,
    options: ArchitectureOptions | None = None,
) -> list[ParadigmResult]:
    options = options or ArchitectureOptions()
    project_root = project_root.resolve()
    context_names = options.contexts or infer_layered_context_names(project_root)
    if not context_names:
        context_names = ["core"]

    file_changes: list[FileChange] = []
    for context_name in context_names:
        context_dir = project_root / "contexts" / context_name
        file_changes.extend(
            [
                _change_for_file(context_dir / "__init__.py", ""),
                _change_for_file(context_dir / "domain" / "__init__.py", _domain_init_text(context_name)),
                _change_for_file(context_dir / "domain" / "entities.py", _entities_text(context_name)),
                _change_for_file(context_dir / "domain" / "events.py", _events_text(context_name)),
                _change_for_file(context_dir / "application" / "__init__.py", _application_init_text(context_name)),
                _change_for_file(context_dir / "application" / "ports.py", _ports_text(context_name)),
                _change_for_file(context_dir / "application" / "services.py", _services_text(context_name)),
                _change_for_file(
                    context_dir / "infrastructure" / "__init__.py",
                    _infrastructure_init_text(context_name),
                ),
                _change_for_file(context_dir / "infrastructure" / "repositories.py", _repositories_text(context_name)),
                _change_for_file(context_dir / "presentation" / "__init__.py", _presentation_init_text(context_name)),
                _change_for_file(context_dir / "presentation" / "controllers.py", _controllers_text(context_name)),
            ]
        )

    file_changes.extend(
        [
            _change_for_file(project_root / "contexts" / "__init__.py", ""),
            _change_for_file(project_root / "shared" / "__init__.py", ""),
            _change_for_file(project_root / "shared" / "event_bus.py", _event_bus_text()),
            _change_for_file(project_root / "shared" / "pipeline.py", _pipeline_text()),
        ]
    )
    file_changes = [change for change in file_changes if change.before_text != change.after_text]
    if options.validate:
        validate_split_outputs(file_changes)
    if not options.preview:
        for change in file_changes:
            change.path.parent.mkdir(parents=True, exist_ok=True)
            write_text_preserving_newlines(change.path, change.after_text)
    violations = _lint_layer_dependencies(project_root)

    return [
        ParadigmResult(
            module_file=project_root,
            class_name="LayeredArchitecture",
            function_names=context_names,
            skipped=violations,
            preview=options.preview,
            file_changes=file_changes,
            preview_diffs=_build_preview_diffs(file_changes) if options.preview else [],
        )
    ]


def infer_layered_context_names(project_root: Path) -> list[str]:
    names: set[str] = set()
    ignored = {
        ".git",
        ".venv",
        "__pycache__",
        ".pytest_cache",
        ".mypy_cache",
        ".ruff_cache",
        "build",
        "contexts",
        "dist",
        "shared",
    }
    for file_path in project_root.glob("*.py"):
        if file_path.name == "__init__.py" or file_path.name.startswith("."):
            continue
        names.add(_safe_context_name(file_path.stem))
    for directory in project_root.iterdir() if project_root.exists() else []:
        if not directory.is_dir() or directory.name in ignored or directory.name.startswith("."):
            continue
        if (directory / ".msignore").exists():
            continue
        if any(directory.glob("*.py")):
            names.add(_safe_context_name(directory.name))
    return sorted(names)


def _change_for_file(path: Path, after_text: str) -> FileChange:
    before_text = path.read_text(encoding="utf-8") if path.exists() else ""
    return FileChange(path=path, existed_before=path.exists(), before_text=before_text, after_text=after_text)


def _safe_context_name(value: str) -> str:
    normalized = re.sub(r"[^a-zA-Z0-9_]+", "_", value).strip("_").lower()
    return normalized or "core"


def _class_prefix(context_name: str) -> str:
    return "".join(part[:1].upper() + part[1:] for part in context_name.split("_") if part) or "Core"


def _domain_init_text(context_name: str) -> str:
    prefix = _class_prefix(context_name)
    return (
        f"from .entities import {prefix}Aggregate, {prefix}Entity\n"
        f"from .events import {prefix}DomainEvent\n\n"
        f"__all__ = [\"{prefix}Aggregate\", \"{prefix}DomainEvent\", \"{prefix}Entity\"]\n"
    )


def _application_init_text(context_name: str) -> str:
    prefix = _class_prefix(context_name)
    return (
        f"from .ports import {prefix}Repository\n"
        f"from .services import {prefix}Service\n\n"
        f"__all__ = [\"{prefix}Repository\", \"{prefix}Service\"]\n"
    )


def _infrastructure_init_text(context_name: str) -> str:
    prefix = _class_prefix(context_name)
    return f"from .repositories import InMemory{prefix}Repository\n\n__all__ = [\"InMemory{prefix}Repository\"]\n"


def _presentation_init_text(context_name: str) -> str:
    prefix = _class_prefix(context_name)
    return f"from .controllers import {prefix}Controller\n\n__all__ = [\"{prefix}Controller\"]\n"


def _entities_text(context_name: str) -> str:
    prefix = _class_prefix(context_name)
    return f'''{ARCHITECTURE_MARKER}
from __future__ import annotations

from dataclasses import dataclass, field, replace
from typing import Any

from .events import {prefix}DomainEvent


@dataclass(frozen=True, slots=True)
class {prefix}Entity:
    id: str

    def evolve(self, **changes: Any) -> "{prefix}Entity":
        return replace(self, **changes)


@dataclass(frozen=True, slots=True)
class {prefix}Aggregate({prefix}Entity):
    events: tuple[{prefix}DomainEvent, ...] = field(default_factory=tuple)

    def record_event(self, event: {prefix}DomainEvent) -> "{prefix}Aggregate":
        return replace(self, events=(*self.events, event))
'''


def _events_text(context_name: str) -> str:
    prefix = _class_prefix(context_name)
    return f'''{ARCHITECTURE_MARKER}
from __future__ import annotations

from dataclasses import dataclass, field
from datetime import datetime, timezone
from typing import Any
from uuid import uuid4


@dataclass(frozen=True, slots=True)
class {prefix}DomainEvent:
    name: str
    payload: dict[str, Any] = field(default_factory=dict)
    event_id: str = field(default_factory=lambda: str(uuid4()))
    occurred_at: datetime = field(default_factory=lambda: datetime.now(timezone.utc))
'''


def _services_text(context_name: str) -> str:
    prefix = _class_prefix(context_name)
    return f'''{ARCHITECTURE_MARKER}
from __future__ import annotations

from collections.abc import Iterable
from dataclasses import dataclass

from shared.event_bus import EventBus
from shared.pipeline import pipe

from ..domain.entities import {prefix}Aggregate
from ..domain.events import {prefix}DomainEvent
from .ports import {prefix}Repository


@dataclass(slots=True)
class {prefix}Service:
    repository: {prefix}Repository
    event_bus: EventBus[{prefix}DomainEvent]

    def save(self, aggregate: {prefix}Aggregate) -> {prefix}Aggregate:
        saved = self.repository.save(aggregate)
        self.publish(saved.events)
        return saved

    def publish(self, events: Iterable[{prefix}DomainEvent]) -> None:
        for event in events:
            self.event_bus.publish(event)

    def run_pipeline(self, value, *steps):
        return pipe(value, *steps)
'''


